// Type helper: extracts dotted-path keys from the zh dictionary (both are identical shape).
type Paths<T, P extends string = ""> = T extends object
  ? {
      [K in keyof T & string]: Paths<
        T[K],
        `${P}${P extends "" ? "" : "."}${K}`
      >;
    }[keyof T & string]
  : P;

export type DictPath = Paths<typeof zh>;
